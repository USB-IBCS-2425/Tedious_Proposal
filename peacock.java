class peacock {

	String name;
	String animal;
	int age;
	Boolean sexmale;
	public peacock(String name, String animal, int age, Boolean sexmale){

		this.name=name;
		this.animal=animal;
		this.age=age;
		this.sexmale=sexmale;
	}
	
	

//how to add them to the enclosure

	public String getname() {
			return name;
		}

	public String getanimal() {
			return animal; 
		}

	public int getage() {
			return age;
		}

	public Boolean sexmale() {
		return sexmale;
			
	}

	










}


